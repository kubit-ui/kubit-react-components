/**
 * Button type
 * @enum {string}
 * @property {string} BUTTON - Button type
 * @property {string} RESET - Reset type
 *  @property {string} SUBMIT - Submit type
 * @example
 * <Button type={ButtonType.BUTTON} />
 *
 */
export enum ButtonType {
  BUTTON = 'button',
  RESET = 'reset',
  SUBMIT = 'submit',
}
